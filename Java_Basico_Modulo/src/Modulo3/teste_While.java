public class teste_While {
    public static void main(String[] args) {
        
        // exemplo de while
        
        int x =0;
        
        while (x <10) {
            System.out.println("NUMERO "+x);
            x++;
            
        }
        int n = x;
        //exemplo de Do_ while
        do {
            System.out.println("NUMERO "+n);
            n++;
        } while (n<=20);
        
        
        
    }
    
}
